Herança: é o ato de herda algo da classe acima
Superclasse é aquela classe que será derivada, é a classe mãe ou base como também é chamada. 
Subclasse é a classe derivada, é a classe filha que foi herdada de uma superclasse.

Encapsulamento:  serve para controlar o acesso aos atributos e métodos de uma classe. 
É uma forma eficiente de proteger os dados manipulados dentro da classe, além de determinar 
onde esta classe poderá ser manipulada.

O Polimorfismo é um mecanismo por meio do qual selecionamos as funcionalidades utilizadas de 
forma dinâmica por um programa no decorrer de sua execução. Com o Polimorfismo, os mesmos 
atributos e objetos podem ser utilizados em objetos distintos, porém, com implementações 
lógicas diferentes.

Overrinding: é quando a subclasse herda algo da superclasse, e através desse metodo é possivel
sobescrever este metodo.

Super: é quando oque foi herdado da superclasse é utilizado, porém é possivel adicionar outro metodo.

